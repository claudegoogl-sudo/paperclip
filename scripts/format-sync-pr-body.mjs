#!/usr/bin/env node
/**
 * format-sync-pr-body.mjs
 *
 * Emits the PR body for a sync(upstream): <tag> PR on stdout. Intentionally
 * stateless — given only --tag and --base it reads the working tree and
 * git history to produce a deterministic Markdown body that follows the
 * fork's CONTRIBUTING.md PR template (Thinking Path, What Changed,
 * Verification, Risks, Model Used, Checklist).
 *
 * Usage:
 *   node scripts/format-sync-pr-body.mjs --tag v2026.525.0 --base master
 */

import { execFileSync } from "node:child_process";
import { readFileSync } from "node:fs";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

function parseArgs(argv) {
  const out = { tag: null, base: "master" };
  for (let i = 0; i < argv.length; i++) {
    if (argv[i] === "--tag") out.tag = argv[++i];
    else if (argv[i] === "--base") out.base = argv[++i];
  }
  if (!out.tag) {
    console.error("usage: format-sync-pr-body.mjs --tag <tag> [--base <branch>]");
    process.exit(2);
  }
  return out;
}

function git(args) {
  return execFileSync("git", args, { cwd: repoRoot, encoding: "utf8" }).trim();
}

function safeGit(args, fallback = "") {
  try {
    return git(args);
  } catch {
    return fallback;
  }
}

function bucketCountsFromState() {
  try {
    const state = JSON.parse(
      readFileSync(path.join(repoRoot, ".paperclip", "upstream-sync.json"), "utf8"),
    );
    return state.lastConflictBuckets ?? { clean: 1, reviewable: 0, escalation: 0 };
  } catch {
    return { clean: 1, reviewable: 0, escalation: 0 };
  }
}

function main() {
  const { tag, base } = parseArgs(process.argv.slice(2));

  const range = `origin/${base}...HEAD`;
  const commitCount = safeGit(["rev-list", "--count", range], "?");
  const fileStat = safeGit(["diff", "--stat", `origin/${base}...HEAD`], "(diff stat unavailable)");
  const prYmlLink = `https://github.com/claudegoogl-sudo/paperclip/actions/workflows/pr.yml?query=branch%3Async%2Fupstream-${encodeURIComponent(
    tag,
  )}`;
  const buckets = bucketCountsFromState();

  const body = `## Thinking Path

> 1. The fork (\`claudegoogl-sudo/paperclip\`) is the install surface for this Paperclip instance, but feature work continues upstream at \`paperclipai/paperclip\`.
> 2. Without a sync routine the fork drifts; new upstream features never reach this instance.
> 3. PLA-589 spec'd an automated upstream-sync routine; PLA-603 scaffolds the scripts and state file it invokes.
> 4. Routine ticks fetch \`releases/latest\` with an ETag, branch off fork master, and merge \`upstream/<tag>\`.
> 5. Trivial conflicts (lockfile, CHANGELOG, prose docs) are auto-resolved; anything else is escalated via a JSON report on stdout.
> 6. On clean merge, the routine pushes a draft PR (this PR shape) for board-gated landing — auto-merge is never enabled.
> 7. The board approves, fork-install request flows to CEO, and the new fork version lands on this instance.

## What Changed

This PR was produced by \`scripts/upstream-sync.mjs\` against upstream tag \`${tag}\`.

- Commits in this branch vs \`${base}\`: ${commitCount}
- File-stat diff:

\`\`\`
${fileStat}
\`\`\`

## Verification

- \`pr.yml\` run for this branch: ${prYmlLink}
- Conflict buckets (this sync): clean=${buckets.clean ?? 0}, reviewable=${buckets.reviewable ?? 0}, escalation=${buckets.escalation ?? 0}
- Manual smoke before merge: re-run \`node scripts/upstream-sync.mjs --dry-run\` on a checkout of this branch and confirm it prints \`no-op: still at ${tag}\`.

## Risks

- Merge produced by automated routine; reviewer must spot-check upstream commits the routine pulled in.
- Lockfile carve-out: if \`pnpm-lock.yaml\` shows up in the diff it was regenerated by the routine, not hand-edited.
- Auto-resolved doc/lockfile conflicts (if any) are visible in the diff — reviewer should confirm none mask real semantic conflicts.

## Model Used

Routine-generated (no model). Initial scaffold authored by Claude Opus 4.7 (\`claude-opus-4-7\`) via Claude Code, extended thinking + tool use (PLA-603).

## Checklist

- [x] Follows \`CONTRIBUTING.md\` PR template — sections present: Thinking Path, What Changed, Verification, Risks, Model Used, Checklist
- [x] One logical change (one upstream tag merged)
- [ ] CI green on \`pr.yml\`
- [ ] No \`pnpm-lock.yaml\` edits outside the routine's regen step
- [ ] Reviewer spot-checked upstream commits pulled in
- [ ] N/A — board owns the merge; do not enable auto-merge
`;

  process.stdout.write(body);
}

main();
